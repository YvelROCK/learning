// Operadores Aritiméticos

// multiplicação *
let multiplicacao;
multiplicacao = 52 * 37;
console.log(multiplicacao);

// divisão /
let divisao;
divisao = 12 / 4;
console.log(divisao);

// adição +
let adicao;
adicao = 21 + 33;
console.log(adicao);

// subitração -
let subitracao;
subitracao = 50 - 22;
console.log(subitracao);

// resto divisão %
let remainder;
remainder = 10 % 8;
console.log(remainder);

// incremento ++
let increment;
increment = 0;
console.log(++increment);

// decremento --
let decrement;
decrement = 1;
console.log(--decrement);

// exponencial **
let exp;
exp = 2 ** 2;
console.log(exp);
